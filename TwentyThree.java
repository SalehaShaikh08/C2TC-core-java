
//TwentyThree

public class TwentyThree {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {//Nested for loop
                System.out.println("Value of i:" + i + " and value of j:" + j);
            }
            System.out.println("************************");
        }
    }
}
