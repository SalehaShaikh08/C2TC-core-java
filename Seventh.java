
//Seveth using Arithmetic Operators

public class Seventh {
    public static void main(String[] args) {

        //Declaration of variable x and y
        int FirstNumber = 10, SecondNumber = 20;

        System.out.println(FirstNumber + SecondNumber);  //Addition of FirstNumber & SecondNumber
        System.out.println(--FirstNumber);  //Decreament of FirstNumber(ie.first decreamented and then printed)
        System.out.println(++SecondNumber); //Increament of SecondNumber(ie. first increamented and then printed)

    }

}
